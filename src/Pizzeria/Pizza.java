package Pizzeria;

public class Pizza {
		String dough, sauce, topping;
		Double diameter;
		int amount;
		boolean coupon;
		public Pizza(String dough, String sauce, String topping, Double diameter, int amount, boolean coupon) {
			this.dough = dough;
			this.sauce = sauce;
			this.topping = topping;
			this.diameter = diameter;
			this.amount = amount;
			this.coupon = coupon;
			
	}
}
